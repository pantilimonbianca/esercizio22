public class WordGames {

    /**
     * This method print "Hello" with a word
     * @param word the word for the concat hello
     * @return a string with a hello + word
     */
    public String addHelloWord(String word){
        return "Hello " + word;
    }

    /**
     * This method print a string with name and surname
     * @param name the string name
     * @param surname the string surname
     * @return return a string with name and surname
     */
    public String getFullName(String name, String surname){
        return name + " " + surname;
    }
}
